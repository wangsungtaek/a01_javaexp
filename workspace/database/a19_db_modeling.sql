		업무 프로세스를 통해서 도출되는 스키마를 추출하여야 한다.
		테이블과 테이블의 관계 설정
		ex) 회원테이블과 물품 테이블 사이의 구매정보 테이블 설정.
		구매정보에서 관리할 내용
		0. 추후 다른 테이블과 데이터를 연관관계를 설정할 때는 key가 필요로 한다.
			ex) 아래 회원 id/물건 id를 매핑해서 key로 설정하든지
