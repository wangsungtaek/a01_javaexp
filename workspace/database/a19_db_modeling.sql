# 데이터 모델링과 데이터 모델의 개념
1. 데이터 모델링(modeling)
	1) 현실 세계의 존재하는 데이터를 컴퓨터 세계의 데이터베이스로 옮기는 변환 과정
	2) 데이터 베이스 모델링의 핵심 과정
	ex) 코끼리(현실세계) ==> 필요한 데이터를 추상화 
		==> 발이 4개, 코의 길이가 @@cm, 몸무게 @@톤 (관리할 목록을 정리하여 추상화)
		==> 모델링(코끼리의 특징 발이 4개, 코의 길이, 무게를 속성으로 하고 이 속성들을
			포함하는 코끼리 엔티티를 설정을 위하여 정리)
2. 2단계 모델링
	1) 개념적 데이터 모델링(요구사항정의서에서 여러 속성 분석)
		현실 세계의 중요 데이터를 추출하여 개념 세계로 옮기는 작업을 말한다.
		ex) 온라인상의 구매 사이트/영화 상영 사이트
		요구사항 정의서 : 회원가입제로 가입정보로 @@@, ... 필요로 하여 가입한다
		회원은 구매/영화를 볼 때, 포인트를 구매 후, 물건을 구매할 때
		포인트가 차감이되고 구매 포인트가 10%로 다시 되돌려 받을 수 있다.
		회원가입을 한다(회원아이디-중복불가, 패스워드8~16, 이름, 주소, 포인트,
			권한, 전화번호, 성인인증)
	2) 논리적 데이터 모델
